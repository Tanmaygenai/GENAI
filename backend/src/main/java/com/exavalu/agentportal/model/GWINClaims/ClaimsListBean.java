
package com.exavalu.agentportal.model.GWINClaims;


public class ClaimsListBean {


    private String id;

    private String claimNumber;

    private String lossDt;

    private String shortDesc;

    private String statusCd;

    private String phoneNumber;

    private String assignedAdjustor;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClaimNumber() {
        return claimNumber;
    }

    public void setClaimNumber(String claimNumber) {
        this.claimNumber = claimNumber;
    }

    public String getLossDt() {
        return lossDt;
    }

    public void setLossDt(String lossDt) {
        this.lossDt = lossDt;
    }

    public String getShortDesc() {
        return shortDesc;
    }

    public void setShortDesc(String shortDesc) {
        this.shortDesc = shortDesc;
    }

    public String getStatusCd() {
        return statusCd;
    }

    public void setStatusCd(String statusCd) {
        this.statusCd = statusCd;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getAssignedAdjustor() {
        return assignedAdjustor;
    }

    public void setAssignedAdjustor(String assignedAdjustor) {
        this.assignedAdjustor = assignedAdjustor;
    }

}
