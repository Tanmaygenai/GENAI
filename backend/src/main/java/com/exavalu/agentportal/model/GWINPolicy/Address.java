
package com.exavalu.agentportal.model.GWINPolicy;
public class Address {
    private String id;
    private String addrTypeCd;
    private String addr1;

    private String city;

    private String stateProvCd;

    private String postalCode;

    private String county;

    private String regionCd;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAddrTypeCd() {
        return addrTypeCd;
    }

    public void setAddrTypeCd(String addrTypeCd) {
        this.addrTypeCd = addrTypeCd;
    }

    public String getAddr1() {
        return addr1;
    }

    public void setAddr1(String addr1) {
        this.addr1 = addr1;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getStateProvCd() {
        return stateProvCd;
    }

    public void setStateProvCd(String stateProvCd) {
        this.stateProvCd = stateProvCd;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getRegionCd() {
        return regionCd;
    }

    public void setRegionCd(String regionCd) {
        this.regionCd = regionCd;
    }




}
